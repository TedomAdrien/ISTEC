                    <h3>L’historique les circonstances de la création de l’Institut Supérieur des Techniques Economiques et Comptables.</h3>
                    <p>L’ Institut Supérieur des Techniques Economiques et Comptables en abrégé ISTEC, crée par arrêté N°08/0269/MINESUP du 03 octobre 2008, sous la tutelle académique de l’Université de DSCHANG en accord avec son promoteur expert-comptable David FOTSO dans le souci d’une plus grande professionnalisation des étudiants et concomitamment de palier l’inadéquation formation/emploi, a décidé d’instaurer en son  sein multiples types de formation et d’unité de valeur , capable d’assurer non seulement une « tête bien pleine mais aussi bien faite » . Institution bénéficiant des formations et enseignements de qualité, subdivisée en plusieurs niveaux, filières et spécialités. Nous avons BTS : les filières Gestion et Commerce avec pour spécialités : (Gestion Logistique et Transport (GLT) Comptabilité et Gestion des Entreprises (CGE), Marketing Commerce vente (MCV), Assistant Manager (AM)…) et les filières Industrielles et technologiques : (Génie Civil (BAT), Electrotechnique (ET), Chaudronnerie (CH) etc…LICENCE : Comptabilité Contrôle et Audit  (CCA), Logistique et Transport (LT) Marketing Manager Opérationnel (MMO) …MASTER : Contrôle et Gestion (CG) …ISTEC vise à donner à ceux-ci une armada d’outils et de connaissances  professionnelles susceptibles de mieux aborder le monde professionnel. ISTEC vise également à donner un ensemble de savoir-faire à partager.
