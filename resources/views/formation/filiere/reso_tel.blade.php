          <li>Maitriser les techniques d’émission, de transmission et de réception des signaux, images, sons ;</li>
          <li>Installer et de configurer le matériel et les logiciels des réseaux et des systèmes de télécommunications;</li>
          <li>Appliquer le contrôle de qualité ;</li>
          <li>Appliquer les procédures d’exploitation ;</li>
          <li>Optimiser le fonctionnement d’un réseau et les protocoles usuels employés ;</li>
          <li>Réaliser et administrer les réseaux ;</li>
          <li>Participer à l’élaboration d’un cahier des charges et de contribuer aux spécifications de la topologie d’un réseau ou d’une installation de télécommunications, de gérer et de faire évoluer   de   telles   installations   en   choisissant   les   équipements   et   les   logiciels appropriés ;</li>
          <li>Coordonner les travaux de remise en état des installations après les incidents, incendie, etc...</li>
