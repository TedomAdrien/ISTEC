        <p>Le <span>Brevet de Technicien Superieur </span>en <span>Télécommunication</span> forme les techniciens supérieurs à même d’assurer la mise en œuvre, l’administration et la maintenance de tout équipement de réseau ou de télécommunication. Leurs compétences dans les différentes technologies de transmission de l’information ain si que leurs connaissances approfondies des protocoles des différents réseaux de communication leur permettent de  concevoir, dans une  équipe, des  architectures  réseau  ou  des  installations  de télécommunications complexes et de les optimiser.</p>
