        <h2>Description:</h2>
        <p>Le <span>Brevet de Technicien Superieur </span>en <span>Réseau Et Sécurité</span> conduit à la formation des techniciens supérieurs, capables de mettre en place un réseau interne à l'entreprise qui est confronté à une évolution majeure des technologies de la communication entrainant une mutation des méthodes de  travail dans  l'entreprise. Les contraintes technologiques liées à ces bouleversements sont multiples, à l’instar des problèmes cruciaux de la sauvegarde sécurisée et du stockage des données, de l’aspect fondamental de la sécurité vis-à-vis du monde Internet du fait de la banalisation de la mobilité et du télétravail, de la sécurisation des flux (cryptage) mais aussi de la nécessité d'authentification renforcée des accès aux données de l'entreprise.</p>
